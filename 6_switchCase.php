<?php
// Here are example of switch case 
$age=1000;
switch ($age) {
    case '10':
        echo "Your age is 10<br>";
        break;
    case '102':
        echo "Your age is 102<br>";
        break;
    case '30':
        echo "Your age is 30<br>";
        break;
    
    default:
        echo "Your age is not match any cased <br>";
        break;
}

?>